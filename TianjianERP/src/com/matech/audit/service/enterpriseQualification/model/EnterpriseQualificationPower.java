package com.matech.audit.service.enterpriseQualification.model;

public class EnterpriseQualificationPower {
	private String uuid;
	private String enterpriseQualificationId;
	private String powerId;
	private String powerType;
	private String modelName;
	private String property;
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getEnterpriseQualificationId() {
		return enterpriseQualificationId;
	}
	public void setEnterpriseQualificationId(String enterpriseQualificationId) {
		this.enterpriseQualificationId = enterpriseQualificationId;
	}
	public String getPowerId() {
		return powerId;
	}
	public void setPowerId(String powerId) {
		this.powerId = powerId;
	}
	public String getPowerType() {
		return powerType;
	}
	public void setPowerType(String powerType) {
		this.powerType = powerType;
	}
	public String getModelName() {
		return modelName;
	}
	public void setModelName(String modelName) {
		this.modelName = modelName;
	}
	public String getProperty() {
		return property;
	}
	public void setProperty(String property) {
		this.property = property;
	}
	
}
