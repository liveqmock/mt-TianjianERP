package com.matech.audit.service.education.model;

/**
 * 培训汇总类
 * @author Administrator
 *
 */
public class EducationPO {
	private String id;
	private String name;
	private String departmentName;
	private String className;
	private String courseType;
	private String registerStartTime;
	private String registerEndTime;
	private String peiType;
	private String trainObject;
	private String registNum;
	private String limitNum;
	private String state;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getCourseType() {
		return courseType;
	}
	public void setCourseType(String courseType) {
		this.courseType = courseType;
	}
	public String getRegisterStartTime() {
		return registerStartTime;
	}
	public void setRegisterStartTime(String registerStartTime) {
		this.registerStartTime = registerStartTime;
	}
	public String getRegisterEndTime() {
		return registerEndTime;
	}
	public void setRegisterEndTime(String registerEndTime) {
		this.registerEndTime = registerEndTime;
	}
	public String getPeiType() {
		return peiType;
	}
	public void setPeiType(String peiType) {
		this.peiType = peiType;
	}
	public String getTrainObject() {
		return trainObject;
	}
	public void setTrainObject(String trainObject) {
		this.trainObject = trainObject;
	}
	public String getRegistNum() {
		return registNum;
	}
	public void setRegistNum(String registNum) {
		this.registNum = registNum;
	}
	public String getLimitNum() {
		return limitNum;
	}
	public void setLimitNum(String limitNum) {
		this.limitNum = limitNum;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
