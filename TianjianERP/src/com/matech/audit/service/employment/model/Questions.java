package com.matech.audit.service.employment.model;

public class Questions {
	private String id;
	private String linkUserId;
	private String honorsObtained;
	private String personality;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getLinkUserId() {
		return linkUserId;
	}
	public void setLinkUserId(String linkUserId) {
		this.linkUserId = linkUserId;
	}
	public String getHonorsObtained() {
		return honorsObtained;
	}
	public void setHonorsObtained(String honorsObtained) {
		this.honorsObtained = honorsObtained;
	}
	public String getPersonality() {
		return personality;
	}
	public void setPersonality(String personality) {
		this.personality = personality;
	}
}
