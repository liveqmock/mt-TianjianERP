package com.matech.audit.service.datamanage.model;

public class BackupManuScript {
	private String taskId;

	private String backupTaskCode;

	private String backupTaskName;

	private String backupUdate;

	private String taskCode;

	private String taskName;

	private String udate;

	public String getBackupTaskCode() {
		return backupTaskCode;
	}

	public void setBackupTaskCode(String backupTaskCode) {
		this.backupTaskCode = backupTaskCode;
	}

	public String getBackupTaskName() {
		return backupTaskName;
	}

	public void setBackupTaskName(String backupTaskName) {
		this.backupTaskName = backupTaskName;
	}

	public String getBackupUdate() {
		return backupUdate;
	}

	public void setBackupUdate(String backupUdate) {
		this.backupUdate = backupUdate;
	}

	public String getTaskCode() {
		return taskCode;
	}

	public void setTaskCode(String taskCode) {
		this.taskCode = taskCode;
	}

	public String getTaskId() {
		return taskId;
	}

	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}

	public String getTaskName() {
		return taskName;
	}

	public void setTaskName(String taskName) {
		this.taskName = taskName;
	}

	public String getUdate() {
		return udate;
	}

	public void setUdate(String udate) {
		this.udate = udate;
	}
}
